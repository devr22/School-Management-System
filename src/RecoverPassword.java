    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if(adminIdTxt.getText() != null && adminIdTxt.getText().length() <=10){
            if(passwordTxt.getText().length() >=6 && passwordTxt.getText().length() <=10){
                proceedToSave();
            }else{
                JOptionPane.showMessageDialog(null, "Password lenght should be between 6 to 10 character");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Admin Id can't be null\n" + "and it can't be more than 10 characters");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void proceedToSave(){
        try{
            String query = "update SchoolDetail set Password='" + passwordTxt.getText() + "' where Id='" + adminIdTxt.getText() + "'";
            pst = con.prepareStatement(query);
            pst.executeUpdate();
            pst.close();
            callMainFrame();
            JOptionPane.showMessageDialog(null, "Password Changes Successfully");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println("Error in storing values: " + e.getMessage());
        }
    }
    
    private void callMainFrame(){
        MainFrame obj = new MainFrame();
        obj.setVisible(true);
        this.dispose();
    }
    
